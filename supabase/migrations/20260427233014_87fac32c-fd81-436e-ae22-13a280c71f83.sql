-- Platforms table - single source of truth for all teacher platforms
CREATE TABLE public.platforms (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  code TEXT UNIQUE NOT NULL, -- e.g. R-9847
  slug TEXT UNIQUE NOT NULL, -- e.g. ahmed-math for /m/ahmed-math
  teacher_name TEXT NOT NULL,
  teacher_phone TEXT NOT NULL,
  subject TEXT NOT NULL, -- math, science, arabic, studies, english
  stage TEXT NOT NULL,   -- prep, secondary, primary
  grade_levels JSONB NOT NULL DEFAULT '[]'::jsonb, -- ["1-prep","2-prep","3-prep"]
  template_tier TEXT NOT NULL DEFAULT 'normal', -- normal | pro
  package_students INTEGER NOT NULL DEFAULT 50,
  package_price INTEGER NOT NULL DEFAULT 500,
  config JSONB NOT NULL DEFAULT '{}'::jsonb, -- colors, icons, animations, name, logo, ai-generated customizations
  status TEXT NOT NULL DEFAULT 'pending', -- pending | approved | active | paused | rejected
  payment_status TEXT NOT NULL DEFAULT 'unpaid', -- unpaid | paid | overdue
  admin_notes TEXT,
  platform_admin_email TEXT, -- teacher's admin email for their platform
  platform_admin_password TEXT, -- simple stored for MVP
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  approved_at TIMESTAMPTZ,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_platforms_slug ON public.platforms(slug);
CREATE INDEX idx_platforms_status ON public.platforms(status);

-- Student access codes (generated by platform admin/teacher)
CREATE TABLE public.student_codes (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  platform_id UUID NOT NULL REFERENCES public.platforms(id) ON DELETE CASCADE,
  code TEXT NOT NULL,
  is_used BOOLEAN NOT NULL DEFAULT false,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE(platform_id, code)
);

-- Students registered on platforms
CREATE TABLE public.students (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  platform_id UUID NOT NULL REFERENCES public.platforms(id) ON DELETE CASCADE,
  access_code TEXT NOT NULL,
  full_name TEXT NOT NULL,
  phone TEXT NOT NULL,
  grade_level TEXT NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE(platform_id, access_code)
);

CREATE INDEX idx_students_platform ON public.students(platform_id);

-- Content table (videos, pdfs, questions, exams)
CREATE TABLE public.content (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  platform_id UUID NOT NULL REFERENCES public.platforms(id) ON DELETE CASCADE,
  kind TEXT NOT NULL, -- video | pdf | question | exam
  title TEXT NOT NULL,
  grade_level TEXT NOT NULL,
  unit TEXT,
  lesson TEXT,
  data JSONB NOT NULL DEFAULT '{}'::jsonb, -- url, options, correct_answer, etc
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_content_platform ON public.content(platform_id);
CREATE INDEX idx_content_kind ON public.content(kind);

-- Exam results
CREATE TABLE public.exam_results (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  platform_id UUID NOT NULL REFERENCES public.platforms(id) ON DELETE CASCADE,
  student_id UUID NOT NULL REFERENCES public.students(id) ON DELETE CASCADE,
  exam_id UUID NOT NULL REFERENCES public.content(id) ON DELETE CASCADE,
  score INTEGER NOT NULL DEFAULT 0,
  total INTEGER NOT NULL DEFAULT 0,
  answers JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

