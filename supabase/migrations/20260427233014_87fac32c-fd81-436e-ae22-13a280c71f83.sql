-- Public read policies (for MVP: we handle auth in app logic since admin is single hardcoded account)
-- Platforms: anyone can view approved/active platforms (needed for public /m/:slug)
CREATE POLICY "Public can view approved platforms" ON public.platforms
  FOR SELECT USING (status IN ('approved','active','paused'));

CREATE POLICY "Anyone can submit a platform request" ON public.platforms
  FOR INSERT WITH CHECK (true);

-- For MVP admin actions go through edge functions with service role; also allow anon update for simplicity
CREATE POLICY "Anyone can update platforms (MVP)" ON public.platforms
  FOR UPDATE USING (true);

-- Students: anyone can register; anyone on platform can view (MVP)
CREATE POLICY "Public can insert students" ON public.students
  FOR INSERT WITH CHECK (true);
CREATE POLICY "Public can view students" ON public.students
  FOR SELECT USING (true);
CREATE POLICY "Public can delete students" ON public.students
  FOR DELETE USING (true);

-- Content: public read for active platforms, public write (MVP - teacher admin UI)
CREATE POLICY "Public can view content" ON public.content
  FOR SELECT USING (true);
CREATE POLICY "Public can insert content" ON public.content
  FOR INSERT WITH CHECK (true);
CREATE POLICY "Public can delete content" ON public.content
  FOR DELETE USING (true);
CREATE POLICY "Public can update content" ON public.content
  FOR UPDATE USING (true);

-- Student codes
CREATE POLICY "Public can view student codes" ON public.student_codes
  FOR SELECT USING (true);
CREATE POLICY "Public can insert student codes" ON public.student_codes
  FOR INSERT WITH CHECK (true);
CREATE POLICY "Public can update student codes" ON public.student_codes
  FOR UPDATE USING (true);
CREATE POLICY "Public can delete student codes" ON public.student_codes
  FOR DELETE USING (true);

-- Exam results
CREATE POLICY "Public can insert exam results" ON public.exam_results
  FOR INSERT WITH CHECK (true);
CREATE POLICY "Public can view exam results" ON public.exam_results
  FOR SELECT USING (true);

-- Admins: used only via edge function with service role, no public access
CREATE POLICY "No public access to admins" ON public.admins
  FOR SELECT USING (false);

-- Update trigger
CREATE OR REPLACE FUNCTION public.set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER platforms_updated_at
  BEFORE UPDATE ON public.platforms
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();
